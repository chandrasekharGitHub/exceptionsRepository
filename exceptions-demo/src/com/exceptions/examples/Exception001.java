/*
 * Whenever exception arises, it terminates the program execution, 
 * means it stops the execution of the current java program. 
 * Below example shows what happens when an exception occurs.
 */

package com.exceptions.examples;

public class Exception001 {

	public static void main(String[] args) {
		
		 for(int i=5;i>=0;i--){
	            System.out.println(10/i);
	     }
	     
		 System.out.println("After for loop...");
	}

}
